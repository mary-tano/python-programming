# Математика
print("Введи число: ")
Number1 = int(input())
print ("Введи еще одно число: ")
Number2 = int(input())
print ("А теперь выбери операцию (+,-,*,/): ")
Operator = input()
print ("Результат = ")
if Operator == "+" :
  print(Number1 + Number2)
if Operator == "-" :
  print(Number1 - Number2)
if Operator == "*" :
  print(Number1 * Number2)
if Operator == "/" :
  print(Number1 / Number2)
